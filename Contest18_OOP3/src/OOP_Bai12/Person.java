package OOP_Bai12;

public class Person {
    private String name;
    private String add;

    public Person() {
    }

    public Person(String name, String add) {
        this.name = name;
        this.add = add;
    }

    public String getName() {
        return name;
    }

    public String getAdd() {
        return add;
    }
}
